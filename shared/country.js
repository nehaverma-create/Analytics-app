let nameToCodeMap = null;

function getNameToCodeMap() {
  if (nameToCodeMap) return nameToCodeMap;

  nameToCodeMap = {
    uk: "GB",
    "united kingdom": "GB",
    usa: "US",
    "united states of america": "US",
  };

  for (const code of ISO_COUNTRY_CODES) {
    try {
      const name = regionNames.of(code);
      if (name) {
        nameToCodeMap[name.toLowerCase()] = code;
      }
    } catch {
      // Skip invalid region codes.
    }
  }

  return nameToCodeMap;
}

export function normalizeCountry(value) {
  if (typeof value !== "string") return null;

  const trimmed = value.trim();
  if (!trimmed) return null;

  if (/^[a-z]{2}$/i.test(trimmed)) {
    const code = trimmed.toUpperCase();
    return code === "XX" ? null : code;
  }

  return getNameToCodeMap()[trimmed.toLowerCase()] || null;
}

export function formatCountryLabel(code) {
  if (!code) return "";

  try {
    return regionNames.of(code) || code;
  } catch {
    return code;
  }
}
